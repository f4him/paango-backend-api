const bcrypt = require('bcrypt')
const mongoose = require('mongoose')

const field_agent_schema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    role: {
        type: String,
        required: true
    }
})

field_agent_schema.pre('save', function (next) {
    const user = this

    bcrypt.hash(user.password, 10, function (error, encrypted) {
        user.password = encrypted
        next()
    })
})



module.exports = mongoose.model('field_agents', field_agent_schema)
